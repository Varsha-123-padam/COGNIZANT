public class DataType {
    public static void main(String[] args) {
        // Integer type
        int age = 25;
        
        // Float type (needs 'f' suffix)
        float height = 5.9f;
        
        // Double type
        double weight = 68.5;
        
        // Character type (single quotes)
        char grade = 'A';
        
        // Boolean type
        boolean isStudent = true;
        
        // Display all variables
        System.out.println("Integer (age): " + age);
        System.out.println("Float (height): " + height);
        System.out.println("Double (weight): " + weight);
        System.out.println("Character (grade): " + grade);
        System.out.println("Boolean (isStudent): " + isStudent);
    }
}

